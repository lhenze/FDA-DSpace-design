      <h3>Faculty Digital Archive</h3>
      <p><strong>What is the Faculty Digital Archive (FDA)?</strong><br />
        The Faculty Digital Archive is a highly visible repository of NYU scholarship, allowing digital works—text, audio, video, data, and more—to be reliably shared and securely stored. Collections may be made freely available worldwide, offered to NYU only, or restricted to a specific group. <a href="http://www.nyu.edu/its/faculty/fda"> more info</a></p>
      <p><strong>Who can deposit material in the FDA?</strong><br />
        Full-time faculty may contribute their research—unpublished and, in many cases, published—in the FDA. Departments, centers, or institutes may use the FDA to distribute their working papers, technical reports, or other research material<a href="http://www.nyu.edu/its/faculty/fda"> more info</a></p>
      <p><strong>Who can access the FDA?</strong><br />
        By default, content in the FDA is world-readable, unless faculty collection owners restrict access. <a href="http://www.nyu.edu/its/faculty/fda"> more info</a></p>
      <p><strong>Finding material</strong><br />
        You can browse or search the FDA via the options in the sidebar of every page, then link to what you find using a URL that will always work.<a href="http://www.nyu.edu/its/faculty/fda"> more info</a></p>
      <p><strong>Restricted material</strong><br />
        While most of the collections in the FDA are open to anyone on the internet, some are restricted by their owners. If you have questions about restricted resources, please write to <a href="mailto: archive.help@nyu.edu">archive.help@nyu.edu</a>.</p>
      <p><strong>Questions and Account setup</strong><br />
        For more information on the FDA, to request space for your materials, or for help with an existing collection, e-mail queries to <a href="mailto: archive.help@nyu.edu">archive.help@nyu.edu</a>.</p>
